export const generateProductionMetrics = (): ProductionMetric[] => {
  const metrics: ProductionMetric[] = [];
  const now = new Date();
  
  for (let i = 23; i >= 0; i--) {
    metrics.push({
      timestamp: new Date(now.getTime() - i * 60 * 60 * 1000),
      productionRate: Math.floor(Math.random() * 30) + 70,
      efficiency: Math.floor(Math.random() * 15) + 80,
      defectRate: Math.random() * 3 + 0.5,
      downtime: Math.floor(Math.random() * 10),
    });
  }
  
  return metrics;
};
